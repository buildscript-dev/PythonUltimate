
# Comparison Operator
# Comparision operator use to compare the two or more values
# using true or false values..
# List of all the comparision operators..

num1 = 120
num2 = 240
num3 = 240

# == Equal to 
print("Equal to")
print(num1 == num2)
print(num2 == num3)

# != Not Equal to
print("Not Equal to")
print(num1 != num2)
print(num2 != num3) 

# > Greater than
print("Greater than")
print(num1 > num2)
print(num2 > num1) 
# < Smaller than
print("Smaller than")
print(num1 < num2)
print(num2 < num1) 

# >= Greater than or equal to
print("Greater than or equal to")
print(num2 >= num1)
print(num2 >= num3) 

# <= Less than or equal to
print("Less than or equal to")
print(num1 <= num2)
print(num2 <= num3) 