# Integers:
age = 20
phoneNo = 1234567890
pinCode = 490020

# To check the data type we use type()....
print(age)
print(type(age))
print(phoneNo)
print(type(phoneNo))
print(pinCode)
print(type(pinCode))


# Floats:
marks = 499.5
pi = 3.14
gForce = 9.8

print(marks)
print(type(marks))
print(pi)
print(type(pi))
print(gForce)
print(type(gForce))


# String:
name = "BuildScript"
userName = 'BuildScript'
email = '''BuildScript'''

print(name)
print(type(name))
print(userName)
print(type(userName))
print(email)
print(type(email))


